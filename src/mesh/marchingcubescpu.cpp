std::unique_ptr<Mesh> MarchingCubesCPU::generate(const Cell &cell, const unsigned char max_depth) const
{
    if(multithreaded)
    {
        // TODO
        return nullptr;
    }
    else
    {
        std::unique_ptr<Mesh> ret(new Mesh());

        if(!ret)
            return nullptr;
        
        std::map<unsigned char, std::vector<glm::vec3>> surfaces;
        generate_unthreaded(cell, max_depth, surfaces);

        // Index vertices
        unsigned int total_size = 0;
        unsigned char surface_count = surfaces.size();

        if(surface_count <= 0)
            return nullptr;

        ret->surfaces.reset(new std::vector< std::vector<unsigned int> >());
        ret->surfaces->resize(surface_count);
        
        for(unsigned char i = 0; i < surface_count; i++)
        {
            unsigned int surface_size = surfaces[i].size();
            total_size += surface_size;
            (*ret->surfaces)[i].reserve(surface_size);
        }

        std::map<glm::vec3, unsigned int> indexed;
        ret->vertices.reset(new std::vector<Vertex>());
        ret->vertices->reserve(total_size);
        unsigned int current_index = 0;

        for(int surf_id = 0; surf_id < surface_count; surf_id++)
        {
            std::vector<glm::vec3> *surface = &surfaces[surf_id];
            for(auto vertex = surface->begin(); vertex != surface->end(); vertex++)
            {
                auto it = indexed.find(*vertex);
                if(it == indexed.end())
                {
                    indexed.insert(std::make_pair(*vertex, current_index));
                    ret->vertices->emplace_back(*vertex);
                    (*ret->surfaces)[surf_id].push_back(current_index);
                    current_index++;
                }
                else
                {
                    (*ret->surfaces)[surf_id].push_back(it->second);
                }
            }
            (*ret->surfaces)[surf_id].shrink_to_fit();
        }

        ret->vertices->shrink_to_fit();
        return ret;
    }
}

struct edge_vertex
{
    edge_vertex() {}

    edge_vertex(const glm::vec3 p1, const glm::vec3 p2, const Point &v1, const Point &v2)
    {
        if(approx_equal(v2.isovalue, 0) || approx_equal(v1.isovalue, v2.isovalue))
        {
            this->isovalue = v1.isovalue;
            this->material = v1.material;
            this->position = p1;
        }
        else if(approx_equal(v1.isovalue, 0))
        {
            this->isovalue = v2.isovalue;
            this->material = v2.material;
            this->position = p2;
        }
        else
        {
            float t = (-v1.isovalue) / (v2.isovalue - v1.isovalue);
            if(v1.isovalue - v2.isovalue >= 0) // v1 >= v2
            {
                this->isovalue = v1.isovalue;
                this->material = v1.material;
            }
            else
            {
                this->isovalue = v2.isovalue;
                this->material = v2.material;
            }
            this->position = glm::mix(p1, p2, t);
        }
    }

    edge_vertex interp(const edge_vertex &other) const
    {
        if(approx_equal(this->isovalue, 0) || approx_equal(this->isovalue, other.isovalue))
        {
            return *this;
        }
        else if(approx_equal(other.isovalue, 0))
        {
            return other;
        }
        else
        {
            float t = (-this->isovalue) / (other.isovalue - this->isovalue);
            edge_vertex ret;
            ret.position = glm::mix(this->position, other.position, t);
            ret.isovalue = glm::mix(this->isovalue, other.isovalue, t);
            return ret;
        }
    }

    glm::vec3 position;
    unsigned char material;
    float isovalue;
};

// Paul Bourke's algorithm and edge tables use a clockwise-ordered cube index.
// My cube index is binary-ordered, so it needs to be converted.
char get_clockwise_cube_index(const Cell &cell)
{
    char index = 0;

    if(cell.get_corner(4).isovalue > 0)
        index |= 1;
    if(cell.get_corner(5).isovalue > 0)
        index |= 2;
    if(cell.get_corner(1).isovalue > 0)
        index |= 4;
    if(cell.get_corner(0).isovalue > 0)
        index |= 8;
    if(cell.get_corner(6).isovalue > 0)
        index |= 16;
    if(cell.get_corner(7).isovalue > 0)
        index |= 32;
    if(cell.get_corner(3).isovalue > 0)
        index |= 64;
    if(cell.get_corner(2).isovalue > 0)
        index |= 128;

    return index;
}

void MarchingCubesCPU::generate_unthreaded(const Cell &cell, const unsigned char max_depth, std::map<unsigned char, std::vector<glm::vec3>> &surfaces) const
{
    if(cell.is_leaf() || cell.get_depth() >= max_depth)
    {
        char cubeindex = get_clockwise_cube_index(cell);

        if(edgeTable[cubeindex] == 0)
            return;
        
        edge_vertex vertlist[12];

        const Point *corners[8];
        glm::vec3 corner_pos[8];
        for(int i = 0; i < 8; i++)
        {
            corners[i] = &cell.get_corner(i);
            corner_pos[i] = cell.get_corner_pos(i);
        }

        // Calculate edge vertex points

        #define EDGE_CHECK(index, vert1, vert2)\
        if((edgeTable[cubeindex] & (1 << index)) != 0)\
            new (&vertlist[index]) (edge_vertex) (corner_pos[vert1], corner_pos[vert2], *corners[vert1], *corners[vert2])
        
        EDGE_CHECK(0, 4, 5);
        EDGE_CHECK(1, 5, 1);
        EDGE_CHECK(2, 1, 0);
        EDGE_CHECK(3, 0, 4);
        EDGE_CHECK(4, 6, 7);
        EDGE_CHECK(5, 7, 3);
        EDGE_CHECK(6, 3, 2);
        EDGE_CHECK(7, 2, 6);
        EDGE_CHECK(8, 4, 6);
        EDGE_CHECK(9, 5, 7);
        EDGE_CHECK(10, 1, 3);
        EDGE_CHECK(11, 0, 2);
        
        // Calculate midpoints
        typedef std::pair<unsigned char, unsigned char> EdgePair;
        #define MAKE_EDGE_PAIR(v1, v2) (std::pair<unsigned char, unsigned char>(std::min(v1, v2), std::max(v1, v2)))
        std::map<EdgePair, edge_vertex> midpoints;
        for(int i = 0; i < 12; i++)
        {
            if((edgeTable[cubeindex] & (1 << i)) == 0)
                continue;
            
            for(int j = 0; j < 12; j++)
            {
                if( // Continue cases
                    i == j || // Same edge
                    (edgeTable[cubeindex] & (1 << j)) == 0 || // Other edge isnt valid
                    vertlist[i].material == vertlist[j].material // Same material
                )
                    continue;
                
                EdgePair pair = MAKE_EDGE_PAIR(i, j);
                if(midpoints.find(pair) == midpoints.end())
                    midpoints.insert(std::make_pair(pair, vertlist[i].interp(vertlist[j])));
            }
        }

        // Begin adding triangles
        for(int i = 0; triTable[cubeindex][i] != -1; i += 3)
        {
            #define ADD_TRI(surf_offset, vert1, vert2, vert3)\
            mat = vertlist[triTable[cubeindex][i+surf_offset]].material;\
            surfaces[mat].push_back(vert1);\
            surfaces[mat].push_back(vert2);\
            surfaces[mat].push_back(vert3)

            #define EDGEPOINT(offset) (vertlist[triTable[cubeindex][i+offset]].position)

            #define MIDPOINT(off1, off2) (midpoints[MAKE_EDGE_PAIR(triTable[cubeindex][i+off1], triTable[cubeindex][i+off2])].position)

            // tri_config are the changes of material from vertex to vertex
            int tri_config = 0;
            if(vertlist[triTable[cubeindex][i]].material != vertlist[triTable[cubeindex][i+1]].material)
                tri_config |= 1;
            if(vertlist[triTable[cubeindex][i+1]].material != vertlist[triTable[cubeindex][i+2]].material)
                tri_config |= 2;
            
            unsigned char mat;
            switch(tri_config)
            {
                case 0: // 0 -> 1 -> 2 -> 0 (1 tri)
                {
                    ADD_TRI(0,
                        EDGEPOINT(0),
                        EDGEPOINT(1),
                        EDGEPOINT(2)
                    );
                }
                break;
                case 1: // 0 -x> 1 -> 2 -x> 0 (3 tris)
                {
                    ADD_TRI(0,
                        EDGEPOINT(0),
                        MIDPOINT(0,1),
                        MIDPOINT(0,2)
                    );
                    ADD_TRI(1,
                        MIDPOINT(0,2),
                        MIDPOINT(0,1),
                        EDGEPOINT(2)
                    );
                    ADD_TRI(2,
                        EDGEPOINT(2),
                        MIDPOINT(0,1),
                        EDGEPOINT(1)
                    );
                }
                break;
                case 2: // 0 -> 1 -x> 2 -x> 0 (3 tris)
                {
                    ADD_TRI(0,
                        EDGEPOINT(0),
                        EDGEPOINT(1),
                        MIDPOINT(0,2)
                    );
                    ADD_TRI(1,
                        MIDPOINT(0,2),
                        EDGEPOINT(1),
                        MIDPOINT(1,2)
                    );
                    ADD_TRI(2,
                        MIDPOINT(0,2),
                        MIDPOINT(1,2),
                        EDGEPOINT(2)
                    );
                }
                break;
                case 3: // 0 -x> 1 -x> 2 (SPECIAL CASE)
                {
                    if(vertlist[triTable[cubeindex][i]].material == vertlist[triTable[cubeindex][i+2]].material)
                    { // 0 -x> 1 -x> 2 -> 0 (3 tris)
                        ADD_TRI(0,
                            EDGEPOINT(0),
                            MIDPOINT(0,1),
                            EDGEPOINT(2)
                        );
                        ADD_TRI(1,
                            MIDPOINT(0,1),
                            EDGEPOINT(1),
                            MIDPOINT(1,2)
                        );
                        ADD_TRI(2,
                            EDGEPOINT(2),
                            MIDPOINT(0,1),
                            MIDPOINT(1,2)
                        );
                    }
                    else
                    { // 0 -x> 1 -x> 2 -x> 0 (6 tris)
                        glm::vec3 true_mid = vertlist[triTable[cubeindex][i]].interp(vertlist[triTable[cubeindex][i+1]]).interp(vertlist[triTable[cubeindex][i+2]]).position;
                        ADD_TRI(0,
                            EDGEPOINT(0),
                            MIDPOINT(0,1),
                            MIDPOINT(0,2)
                        );
                        ADD_TRI(0,
                            MIDPOINT(0,2),
                            MIDPOINT(0,1),
                            true_mid
                        );
                        ADD_TRI(1,
                            MIDPOINT(0,1),
                            EDGEPOINT(1),
                            MIDPOINT(1,2)
                        );
                        ADD_TRI(1,
                            true_mid,
                            MIDPOINT(0,1),
                            MIDPOINT(1,2)
                        );
                        ADD_TRI(2,
                            MIDPOINT(0,2),
                            MIDPOINT(1,2),
                            EDGEPOINT(2)
                        );
                        ADD_TRI(2,
                            MIDPOINT(0,2),
                            true_mid,
                            MIDPOINT(1,2)
                        );
                    }
                }
                break;
            }
        }
    }
    else
    {
        for(int i = 0; i < 8; i++)
            generate_unthreaded(*cell.get_child(i), max_depth, surfaces);
    }
}

